/**
 * Comparison
 * ----------
 * 
 * 1. bigger: > 
 * 2. less: <
 * 3. equal: ==
 * 4. greater than or equal: >=
 * 5. less than or equal: <=
 * 6. not equal: !=
 * 
 * 
 * -------------------
 * 7. AND: &&
 * 8. OR: ||
 * 
 */

console.log(5 > 4); // greater
console.log(3 < 4); // less
console.log(5 == 5);  // equal
console.log(5 >= 4);  // greater than or equal
console.log(4 <= 4); // less than or equal
console.log(5 != 4); // not equal

console.log(10 == '10'); // check values only
console.log(10 === '10'); // check values and types